fun main(args: Array<String>) {
    var total = 0
    total = total + 100
    println(total)

    total = total - 33
    println(total)

    total += 33
    println(total)

    total -= 10
    println(total)

    total++
    println(total)

    total--
    println(total)

    var pi : Double = 3.14
    var radius : Int = 3
    var circunference : Int = pi.toInt() * radius

    println(circunference)
}
